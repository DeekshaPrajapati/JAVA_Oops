package ExceptionHandling;

public class DiffernceCheckedUnchecked {
    /*
    Checked Exception/ Compile Time Exception=
    1) Checked exceptions are the exceptions that are checked and handled at compile time.
    2) A checked exception occur when the changes of failure are too high.
    3) They are direct sub-class of exception and they are not inherit from runtime exception.
     */

    /*
    Unchecked Exception/ RunTime Exception=
    1) Unchecked exception are exceptions that are not checked at compile time.
    2) Unchecked exception are occur mostly due to programming mistake.
    3) They are direct sub-class of runtime exception class.
     */
}
